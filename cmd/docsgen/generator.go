type frontmatter struct {
	Title        string `yaml:"title"`
	SidebarTitle string `yaml:"sidebar_title"`
	URL          string `yaml:"url"`
	Section      string `yaml:"section"`
	Description  string `yaml:"description"`
	LastUpdated  string `yaml:"last_updated"`
}

var codeBlockPattern = regexp.MustCompile(`(?s)<pre><code(?: class="language-([^"]+)")?>(.*?)</code></pre>`)

const docsCodeStyle = "github-dark"

func WriteGeneratedFile(repoRoot string) error {
	root, err := filepath.Abs(repoRoot)
	if err != nil {
		return err
	}

	docsDir := filepath.Join(root, "docs", "public")
	outputPath := filepath.Join(root, "internal", "docs", "docs_generated.go")

	files := make([]string, 0)
	err = filepath.WalkDir(docsDir, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if d.IsDir() {
			return nil
		}
		if filepath.Ext(path) != ".md" {
			return nil
		}
		files = append(files, path)
		return nil
	})
	if err != nil {
		return err
	}
	if len(files) == 0 {
		return fmt.Errorf("no docs markdown files found")
	}
	slices.Sort(files)

	renderer := goldmark.New(
		goldmark.WithExtensions(extension.GFM),
		goldmark.WithParserOptions(parser.WithAutoHeadingID()),
		goldmark.WithRendererOptions(mdhtml.WithUnsafe()),
	)

	pages := make([]docs.Page, 0, len(files))
	sections := make([]docs.NavSection, 0)
	pagesByPath := make(map[string]docs.Page, len(files))
	sectionIndexes := make(map[string]int)
	for _, path := range files {
		page, err := parseDocFile(path, renderer)
		if err != nil {
			return err
		}
		if _, exists := pagesByPath[page.Path]; exists {
			return fmt.Errorf("duplicate docs path %q", page.Path)
		}
		pages = append(pages, page)
		pagesByPath[page.Path] = page

		idx, ok := sectionIndexes[page.Section]
		if !ok {
			idx = len(sections)
			sectionIndexes[page.Section] = idx
			sections = append(sections, docs.NavSection{Title: page.Section})
		}
		sections[idx].Pages = append(sections[idx].Pages, docs.NavPage{
			SidebarTitle: page.SidebarTitle,
			Path:         page.Path,
		})
	}

	indexPath := pages[0].Path

	var out bytes.Buffer
	out.WriteString("// Code generated by docsgen; DO NOT EDIT.\n")
	out.WriteString("package docs\n\n")
	out.WriteString("func init() {\n")
	out.WriteString("\tgeneratedSite = Site{\n")
	fmt.Fprintf(&out, "\tIndexPath: %q,\n", indexPath)
	out.WriteString("\tPagesByPath: map[string]Page{\n")
	for _, page := range pages {
		fmt.Fprintf(&out, "\t\t%q: {\n", page.Path)
		fmt.Fprintf(&out, "\t\t\tTitle: %q,\n", page.Title)
		fmt.Fprintf(&out, "\t\t\tSidebarTitle: %q,\n", page.SidebarTitle)
		fmt.Fprintf(&out, "\t\t\tDescription: %q,\n", page.Description)
		fmt.Fprintf(&out, "\t\t\tSection: %q,\n", page.Section)
		fmt.Fprintf(&out, "\t\t\tPath: %q,\n", page.Path)
		fmt.Fprintf(&out, "\t\t\tLastUpdated: %q,\n", page.LastUpdated)
		fmt.Fprintf(&out, "\t\t\tContentHTML: %q,\n", page.ContentHTML)
		out.WriteString("\t\t\tHeadings: []PageHeading{\n")
		for _, heading := range page.Headings {
			fmt.Fprintf(&out, "\t\t\t\t{Title: %q, ID: %q},\n", heading.Title, heading.ID)
		}
		out.WriteString("\t\t\t},\n")
		out.WriteString("\t\t},\n")
	}
	out.WriteString("\t},\n")
	out.WriteString("\tNavSections: []NavSection{\n")
	for _, section := range sections {
		fmt.Fprintf(&out, "\t\t{Title: %q, Pages: []NavPage{\n", section.Title)
		for _, page := range section.Pages {
			fmt.Fprintf(&out, "\t\t\t{SidebarTitle: %q, Path: %q},\n", page.SidebarTitle, page.Path)
		}
		out.WriteString("\t\t}},\n")
	}
	out.WriteString("\t},\n")
	out.WriteString("\t}\n")
	out.WriteString("}\n")

	formatted, err := format.Source(out.Bytes())
	if err != nil {
		return err
	}

	existing, err := os.ReadFile(outputPath)
	if err == nil && bytes.Equal(existing, formatted) {
		return nil
	}
	if err != nil && !os.IsNotExist(err) {
		return err
	}

	return os.WriteFile(outputPath, formatted, 0o644)
}

func parseDocFile(path string, renderer goldmark.Markdown) (docs.Page, error) {
	content, err := os.ReadFile(path)
	if err != nil {
		return docs.Page{}, err
	}
	fm, body, err := splitFrontmatter(string(content))
	if err != nil {
		return docs.Page{}, fmt.Errorf("%s: %w", path, err)
	}

	if fm.Title == "" {
		return docs.Page{}, fmt.Errorf("%s: missing title frontmatter", path)
	}
	if fm.SidebarTitle == "" {
		return docs.Page{}, fmt.Errorf("%s: missing sidebar_title frontmatter", path)
	}
	if fm.URL == "" {
		return docs.Page{}, fmt.Errorf("%s: missing url frontmatter", path)
	}
	if fm.LastUpdated == "" {
		return docs.Page{}, fmt.Errorf("%s: missing last_updated frontmatter", path)
	}

	lastUpdated, err := time.Parse("2 January 2006", fm.LastUpdated)
	if err != nil {
		return docs.Page{}, fmt.Errorf("%s: invalid last_updated frontmatter: %w", path, err)
	}

	body = strings.TrimSpace(strings.ReplaceAll(body, "\r\n", "\n"))

	source := []byte(body)
	context := parser.NewContext()
	doc := renderer.Parser().Parse(text.NewReader(source), parser.WithContext(context))

	var html bytes.Buffer
	if err := renderer.Renderer().Render(&html, source, doc); err != nil {
		return docs.Page{}, err
	}

	contentHTML, err := highlightCodeBlocks(html.String())
	if err != nil {
		return docs.Page{}, err
	}

	return docs.Page{
		Title:        fm.Title,
		SidebarTitle: fm.SidebarTitle,
		Description:  fm.Description,
		Section:      fm.Section,
		Path:         normalizeRoutePath(fm.URL),
		LastUpdated:  lastUpdated.Format("2 January 2006"),
		ContentHTML:  contentHTML,
		Headings:     extractH2Headings(source, doc),
	}, nil
}

func highlightCodeBlocks(content string) (string, error) {
	var highlightErr error

	highlighted := codeBlockPattern.ReplaceAllStringFunc(content, func(block string) string {
		if highlightErr != nil {
			return block
		}

		matches := codeBlockPattern.FindStringSubmatch(block)
		if len(matches) != 3 {
			return block
		}

		language := strings.TrimSpace(matches[1])
		if language == "" {
			return block
		}

		lexer := lexers.Get(language)
		if lexer == nil {
			return block
		}

		iterator, err := chroma.Coalesce(lexer).Tokenise(nil, stdhtml.UnescapeString(matches[2]))
		if err != nil {
			highlightErr = err
			return block
		}

		var rendered bytes.Buffer
		formatter := html.New(
			html.WithClasses(false),
			html.PreventSurroundingPre(false),
		)
		if err := formatter.Format(&rendered, styles.Get(docsCodeStyle), iterator); err != nil {
			highlightErr = err
			return block
		}

		return rendered.String()
	})

	if highlightErr != nil {
		return "", highlightErr
	}

	return highlighted, nil
}

func extractH2Headings(source []byte, doc ast.Node) []docs.PageHeading {
	headings := []docs.PageHeading{}

	_ = ast.Walk(doc, func(n ast.Node, entering bool) (ast.WalkStatus, error) {
		if !entering {
			return ast.WalkContinue, nil
		}

		heading, ok := n.(*ast.Heading)
		if !ok || heading.Level != 2 {
			return ast.WalkContinue, nil
		}

		idValue, ok := heading.AttributeString("id")
		if !ok {
			return ast.WalkContinue, nil
		}

		id, ok := idValue.([]byte)
		if !ok {
			return ast.WalkContinue, nil
		}

		title := strings.TrimSpace(string(heading.Lines().Value(source)))
		if title == "" || len(id) == 0 {
			return ast.WalkContinue, nil
		}

		headings = append(headings, docs.PageHeading{
			Title: title,
			ID:    string(id),
		})

		return ast.WalkContinue, nil
	})

	return headings
}

func splitFrontmatter(content string) (frontmatter, string, error) {
	content = strings.ReplaceAll(content, "\r\n", "\n")
	if !strings.HasPrefix(content, "---\n") {
		return frontmatter{}, "", fmt.Errorf("docs file missing frontmatter opening delimiter")
	}

	rest := strings.TrimPrefix(content, "---\n")
	idx := strings.Index(rest, "\n---\n")
	if idx == -1 {
		return frontmatter{}, "", fmt.Errorf("docs file missing frontmatter closing delimiter")
	}

	var fm frontmatter
	if err := yaml.Unmarshal([]byte(rest[:idx]), &fm); err != nil {
		return frontmatter{}, "", err
	}

	return fm, rest[idx+len("\n---\n"):], nil
}

func normalizeRoutePath(url string) string {
	url = strings.TrimSpace(url)
	url = strings.TrimPrefix(url, "/")
	url = strings.TrimSuffix(url, "/")
	if url == "" {
		return "/docs"
	}
	return "/docs/" + url
}
